    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        if(obj == null) {
            String processador = txtProcessador.getText();
            String hd = txtHD.getText();

            Computador objCadastro = new Computador(hd, processador);

            boolean retornoBanco = ComputadorDAO.salvar(objCadastro);

            if(retornoBanco) {
                JOptionPane.showMessageDialog(rootPane,"Computador cadastrado com sucesso!");
            } else {
                JOptionPane.showMessageDialog(rootPane,"Falha ao cadastrar...");
            }
        }
        else {
            String processador = txtProcessador.getText();
            String hd = txtHD.getText();
            obj.setHd(hd);
            obj.setProcessador(processador);
            boolean retornoBanco = ComputadorDAO.alterar(obj);
            if(retornoBanco) {
                JOptionPane.showMessageDialog(rootPane,"Computador alterado com sucesso!");
                this.dispose();
                TelaConsultaComputador telaConsulta = new TelaConsultaComputador();
                telaConsulta.atualizarTabela();
                telaConsulta.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(rootPane,"Falha na altera√ß√£o...");
            }
        }
        dispose();
    }//GEN-LAST:event_btnSalvarActionPerformed

