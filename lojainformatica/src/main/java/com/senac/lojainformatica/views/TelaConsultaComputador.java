    public void atualizarTabela() {

        ArrayList<Computador> obj = ComputadorDAO.listar();

        DefaultTableModel model = (DefaultTableModel) tblComputadores.getModel();

        model.setRowCount(0);
        for (Computador computador : obj) {
            model.addRow(new String[]{
                String.valueOf(computador.getIdComputador()),
                String.valueOf(computador.getMarca()),
                String.valueOf(computador.getHd()),
                String.valueOf(computador.getProcessador())});
        }
    }
    
    
    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = tblComputadores.getSelectedRow();
        if (linhaSelecionada >= 0) {
            DefaultTableModel model = (DefaultTableModel) tblComputadores.getModel();
            int idExcluir = Integer.parseInt(model.getValueAt(linhaSelecionada, 0).toString());

            if (ComputadorDAO.excluir(idExcluir)) {
                JOptionPane.showMessageDialog(rootPane, "Item exclu√≠do com sucesso!");

                atualizarTabela();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Erro ao excluir o item...");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione uma linha");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
        // TODO add your handling code here:
        if (txtFiltro.getText().strip().equals("")) {
            atualizarTabela();
        } else {
            String proBuscar = txtFiltro.getText();
            ArrayList<Computador> list = ComputadorDAO.buscarPorProcessador(proBuscar);

            DefaultTableModel model = (DefaultTableModel) tblComputadores.getModel();
            model.setRowCount(0);

            for (Computador computador : list) {
                model.addRow(new String[]{
                    String.valueOf(computador.getIdComputador()),
                    String.valueOf(computador.getMarca()),
                    String.valueOf(computador.getHd()),
                    String.valueOf(computador.getProcessador())
                });

            }
        }
    }//GEN-LAST:event_btnListarActionPerformed

    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        // TODO add your handling code here:
        TelaCadastroComputador t1 = new TelaCadastroComputador();
            t1.setVisible(true);
            atualizarTabela();
    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = tblComputadores.getSelectedRow();
        
        if (linhaSelecionada >= 0) {
            
            DefaultTableModel modelo = (DefaultTableModel) tblComputadores.getModel();
            int id = Integer.parseInt(modelo.getValueAt(linhaSelecionada, 0).toString()); 
            String hd = (modelo.getValueAt(linhaSelecionada, 2).toString());
            String processador = (modelo.getValueAt(linhaSelecionada, 3).toString());

            
            TelaCadastroComputador t1 = new TelaCadastroComputador(id, new Computador(hd, processador));
            t1.setVisible(true);
            atualizarTabela();
            dispose();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione uma linha");
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

